    private void calcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_calcularActionPerformed
  double notas[]= new double[5];
  JTextField textos[]= {txtnota1,txtnota2,txtnota3,txtnota4,txtnota5};
    boolean validar= true;      
  for (int i=0; i<5;i++){
      
      try{
   notas[i]= Double.parseDouble(textos[i].getText());
   if(notas[i]>=0 && notas[i]<=5){
       notas[i]= Double.parseDouble(textos[i].getText());  
   }else{
       joption.showMessageDialog(null, "La calificaciÃ³n " +(i+1)+" ingresada es incorrecta el rango es entre 0 a 5");
      validar=false;
       textos[i].setText(" ");
       notas[i]--;
   
   }
      }
      catch (Exception e){
     joption.showMessageDialog(null, "La calificacion "+(i+1)+"no es un numero");
      validar=false;
       textos[i].setText(" ");
       notas[i]--;
      }
  }
    
  
  
  if(validar==true){
     rendimiento.establecerCalif(notas);
     rendimiento.calcularPromedio();
     rendimiento.calcularDesviacion();
     rendimiento.calcularMax_nota();
     rendimiento.calcularMin_nota();
     nombrealum.setText("El alumno "+txtnombre.getText());
     promedio.setText(String.valueOf(rendimiento.obtenerPromedio()));
     Desviacion.setText(String.valueOf(rendimiento.obtenerDesviacion()));
     mayor.setText(String.valueOf(rendimiento.obtenerMax_nota()));
     menor.setText(String.valueOf(rendimiento.obtenerMin_nota()));
  }
  
     

    }//GEN-LAST:event_calcularActionPerformed

